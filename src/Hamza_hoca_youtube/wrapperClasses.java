package Hamza_hoca_youtube;

public class wrapperClasses {
    public static void main(String[] args) {


        Integer num1 = new Integer(45);
        System.out.println("num1.floatValue() = " + num1.floatValue());

        System.out.println("num1.toString() = " + num1.toString());

        String number = "456";

        Integer num2 = Integer.parseInt(number);
        System.out.println("num2 = " + num2);

        System.out.println("Double.parseDouble(number) = " + Double.parseDouble(number));
    }
}
