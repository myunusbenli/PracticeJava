package day39_wrapper_classes;

public class AutoboxingUnboxing {
    public static void main(String[] args) {


        //auto boxing  ---- primitive type to Object
        int num1 = 500;
        Integer num2 = num1;

        //Unboxing     -----Object to primitive

        Integer num3 = 400;
        int num4 = num3;


    }
}
